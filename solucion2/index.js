document.querySelector("input[data-variant-id='39297873051766']").onclick = function() {


    if(!document.getElementById("message_alert_test")){

        const para = document.createElement("p");
        const node = document.createTextNode("Add 2 of this product and the third one is free!");
        para.setAttribute("id", "message_alert_test");
        para.style.background = "#BAF8E5";
        para.style.width = "500px";
        para.style.color = "gray";
        para.appendChild(node);
        document.getElementsByClassName("tw-w-full tw-my-5")[0].appendChild(para);

        var form1 = document.querySelector("input[data-variant-id='39297872855158']");
        var selectedValue = form1.value;
        console.log(form1)
    }
}

